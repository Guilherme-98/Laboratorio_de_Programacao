import java.math.BigDecimal;

public class Combustivel {
	
private String tipo;
	
	private  BigDecimal price;
	
	
	public String getType() {
		return tipo;
	}

	public void setType(String tipoComb) {
		this.tipo = tipoComb;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal pL) {
		this.price = pL;
	}

}
	
